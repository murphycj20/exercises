public class AverageRain {

	public static void main(String[] args){

		double ans, apr = 12, may = 14, jun = 8;

		ans = (apr + may + jun)/3;

		System.out.println("Rainfall for April:\t" + apr);
		System.out.println("Rainfall for May:\t" + may);
		System.out.println("Rainfall for June:\t" + jun);
		System.out.println("Average Rainfall:\t" + ans);

	}

}