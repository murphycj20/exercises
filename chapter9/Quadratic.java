public class Quadratic
{  
  public static void main ( String[] args )  
  {
    double x, ans;

    x = 0;
    ans = (3 * (x * x)) + (-8 * x) + 4;
    System.out.println("At X = " + x + " the answer is " + ans);

    x = 2;
    ans = (3 * (x * x)) + (-8 * x) + 4;
    System.out.println("At X = " + x + " the answer is " + ans);

    x = 4;
    ans = (3 * (x * x)) + (-8 * x) + 4;
    System.out.println("At X = " + x + " the answer is " + ans);
}

}
